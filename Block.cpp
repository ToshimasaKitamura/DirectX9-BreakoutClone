std::vector<int> Block::_destroyBlockNumber;

void Block::CollisionRegist(E_Stage stage) const
{
	Collision collision;
	for (int i = 0; i < _blockVMax * _blockHMax; ++i)
	{
		const std::string tmp("block" + std::to_string(i));
		const Vector2 vecLU(_blockStartPosX + _blockW * (i % _blockHMax), 
			_blockStartPosY + _blockH * (i / _blockHMax));
		const Vector2 vecRD(_blockStartPosX + _blockW + _blockW * (i % _blockHMax), 
			_blockStartPosY + _blockH + _blockH * (i / _blockHMax));
		if (_blockArray[(int)stage][i]) collision.Regist(tmp, Vector2Tuple2(vecLU, vecRD));
	}
}

void Block::Update(E_Stage stage)
{
	Collision collision;
	
	for (int i = 0; i < _blockVMax * _blockHMax; ++i)
	{
		if (!_blockArray[(int)stage][i]) continue;
		const std::string tmp("block" + std::to_string(i));
		if (collision.isHit(tmp, "ball")) _destroyBlockNumber.push_back(i);
	}
}

void Block::Draw(E_Stage stage) const
{
	for (int i = 0; i < (int)E_Stage::Max; ++i)
	{
		for (int j = 0; j < _blockVMax * _blockHMax; ++j)
		{
			if (_blockArray[(int)stage][j]) TSYGraphics::DrawGraph(_blockStartPosX + _blockW * (j % _blockHMax), _blockStartPosY + _blockH * (j / _blockHMax), "block.png");
		}
	}	
}

void Block::Destroy(E_Stage stage)
{
	for (const auto& e : _destroyBlockNumber)
	{
		_blockArray[(int)stage][e] = false;
	}
	_destroyBlockNumber.clear();
}

void Block::GetActiveBlcok(E_Stage stage, std::array<bool, _blockHMax* _blockVMax>& blockArray)
{
	blockArray = _blockArray[(int)stage];
}

bool Block::isBlockZero(E_Stage stage) const
{
	for (const auto& e : _blockArray[(int)stage])
	{
		if(e) return false;
	}
	return true;
}